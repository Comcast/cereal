package com.comcast.testclasses;

import com.comcast.cereal.Cerealizable;

public class Name implements Cerealizable<String> {

    private String first;
    private String last;

    public Name(String first, String last) {
        this.first = first;
        this.last = last;
    }

    public String getFirst() {
        return first;
    }

    public String getLast() {
        return last;
    }

    public String toCereal() {
        return last + ", " + first;
    }

    public void applyCereal(String cereal) {
        int index = cereal.indexOf(", ");
        this.last = cereal.substring(0, index);
        this.first = cereal.substring(index + 2);
    }

    /*
     * (non-Javadoc)
     * @see java.lang.Object#hashCode()
     */
    @Override
    public int hashCode() {
        final int prime = 31;
        int result = 1;
        result = prime * result + ((first == null) ? 0 : first.hashCode());
        result = prime * result + ((last == null) ? 0 : last.hashCode());
        return result;
    }

    /*
     * (non-Javadoc)
     * @see java.lang.Object#equals(java.lang.Object)
     */
    @Override
    public boolean equals(Object obj) {
        if (this == obj) {
            return true;
        }
        if (obj == null) {
            return false;
        }
        if (!(obj instanceof Name)) {
            return false;
        }
        Name other = (Name) obj;
        if (first == null) {
            if (other.first != null) {
                return false;
            }
        } else if (!first.equals(other.first)) {
            return false;
        }
        if (last == null) {
            if (other.last != null) {
                return false;
            }
        } else if (!last.equals(other.last)) {
            return false;
        }
        return true;
    }

}
