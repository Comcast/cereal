package com.comcast.cvs.testclasses;

import javax.mail.internet.InternetAddress;

import com.comcast.cereal.annotations.Cereal;
import com.comcast.cvs.cereal.engines.EmailCerealizer;

public class Member {

    @Cereal(name = "first.name")
    public String firstName;

    @Cereal(name = "last.name")
    public String lastName;

    @Cereal(cerealizer = EmailCerealizer.class)
    public InternetAddress email;

    @Cereal(defaultValue = "MANAGER")
    public Title title;

    /*
     * (non-Javadoc)
     * @see java.lang.Object#hashCode()
     */
    @Override
    public int hashCode() {
        final int prime = 31;
        int result = 1;
        result = prime * result + ((email == null) ? 0 : email.hashCode());
        result = prime * result + ((firstName == null) ? 0 : firstName.hashCode());
        result = prime * result + ((lastName == null) ? 0 : lastName.hashCode());
        result = prime * result + ((title == null) ? 0 : title.hashCode());
        return result;
    }

    /*
     * (non-Javadoc)
     * @see java.lang.Object#equals(java.lang.Object)
     */
    @Override
    public boolean equals(Object obj) {
        if (this == obj) {
            return true;
        }
        if (obj == null) {
            return false;
        }
        if (!(obj instanceof Member)) {
            return false;
        }
        Member other = (Member) obj;
        if (email == null) {
            if (other.email != null) {
                return false;
            }
        } else if (!email.equals(other.email)) {
            return false;
        }
        if (firstName == null) {
            if (other.firstName != null) {
                return false;
            }
        } else if (!firstName.equals(other.firstName)) {
            return false;
        }
        if (lastName == null) {
            if (other.lastName != null) {
                return false;
            }
        } else if (!lastName.equals(other.lastName)) {
            return false;
        }
        if (title != other.title) {
            return false;
        }
        return true;
    }

}
